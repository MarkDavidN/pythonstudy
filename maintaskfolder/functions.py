#function to calculate area of triangle
def triangleare():
    base=int(input("enter base"))
    height=int(input("enter height"))
    area=(base*height)/2
    print(area)
triangleare()    

#create a function that calculates area of a rectangle
def recarea():
    length=int(input("enter length"))
    width=int(input("width"))
    area=(width*length)
    print(area)
recarea()    